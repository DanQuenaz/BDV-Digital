class Usuario {
    private String motoristaNome;
    private Integer motoristaMatricula;
    private String veiculoCartela;
    private Integer centroCusto;
    private String veiculoPlaca;
    private String veiculoModelo;



    public String getMotoristaNome() {
        return motoristaNome;
    }

    public void setMotoristaNome(String motoristaNome) {
        this.motoristaNome = motoristaNome;
    }

    public Integer getMotoristaMatricula() {
        return motoristaMatricula;
    }

    public void setMotoristaMatricula(Integer motoristaMatricula) {
        this.motoristaMatricula = motoristaMatricula;
    }

    public String getVeiculoCartela() {
        return veiculoCartela;
    }

    public void setVeiculoCartela(String veiculoCartela) {
        this.veiculoCartela = veiculoCartela;
    }

    public Integer getCentroCusto() {
        return centroCusto;
    }

    public void setCentroCusto(Integer centroCusto) {
        this.centroCusto = centroCusto;
    }

    public String getVeiculoPlaca() {
        return veiculoPlaca;
    }

    public void setVeiculoPlaca(String veiculoPlaca) {
        this.veiculoPlaca = veiculoPlaca;
    }

    public String getVeiculoModelo() {
        return veiculoModelo;
    }

    public void setVeiculoModelo(String veiculoModelo) {
        this.veiculoModelo = veiculoModelo;
    }
}
